    public void carregaRank() {
        Object[] lista = null;
        try {
            atendimentoDAO = new AtendimentoDAO();
            for (int i = 0; i < atendimentoDAO.listRankAtendimentosByUsuarios().size(); i++) {
                lista = (Object[]) atendimentoDAO.listRankAtendimentosByUsuarios().get(i);
                model = (DefaultTableModel) tb_rank.getModel();
                model.addRow(lista);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Lista Vazia! \n");
        }
    }

